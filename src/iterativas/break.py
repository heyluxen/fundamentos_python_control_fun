for numero in range(1, 11):
    if numero == 5:
        print("¡Encontrado el 5! Saliendo del bucle...")
        break # Recorre números del 1 al 10 y se detiene cuando encuentra el 5 (usa break).
    print(f"Número actual: {numero}")

print("Bucle terminado") 

# Este ejemplo muestra cómo usar break para detener un bucle al encontrar un valor específico.

