def dividir(dividendo, divisor):  # Función para dividir dos números
    return dividendo / divisor  # Devuelve el resultado

# Uso con argumentos posicionales
resultado1 = dividir(10, 2)  # Pasa valores en orden

# Uso con argumentos por nombre
resultado2 = dividir(divisor=2, dividendo=10)  # Pasa valores indicando el nombre

# Este ejemplo muestra el uso de argumentos posicionales y por nombre en funciones.