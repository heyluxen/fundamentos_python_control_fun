def calcular_cuadrado(numero):  # Función para calcular el cuadrado
    resultado = numero * numero  # Multiplica el número por sí mismo
    return resultado  # Devuelve el resultado

area = calcular_cuadrado(4)  # Calcula el cuadrado de 4
print(area)  # Muestra el resultado

# Este ejemplo calcula el cuadrado de un número usando una función.

# Salida:
# 16