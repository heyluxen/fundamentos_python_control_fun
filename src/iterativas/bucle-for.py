frutas = ["manzana", "banana", "cereza"] # Lista de frutas
for fruta in frutas: # Bucle for para iterar sobre la lista de frutas
    print(fruta) # Imprime cada fruta en la lista

# En este ejemplo, el bucle for recorre cada elemento de la lista "frutas" y lo asigna a la variable "fruta". Luego, se imprime el valor de "fruta" en cada iteración del bucle.

# Salida:
# manzana
# banana
# cereza