for numero in range(1, 11):  # Recorre números del 1 al 10
    if numero % 2 == 0:  # Verifica si el número es par
        continue  # Salta los números pares

    print(f"Número impar: {numero}")  # Muestra solo los impares

# Este ejemplo imprime únicamente los números impares del 1 al 10.

