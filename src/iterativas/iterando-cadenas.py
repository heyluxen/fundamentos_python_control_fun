mensaje = "Python" # Cadena de texto que se va a iterar
for letra in mensaje: # Bucle for que itera sobre cada letra de la cadena "mensaje"
    print(letra) # Imprime cada letra de la cadena en una nueva línea

# En este ejemplo, el bucle for recorre cada carácter de la cadena "mensaje" y lo asigna a la variable "letra". Luego, se imprime el valor de "letra" en cada iteración del bucle, lo que resulta en la impresión de cada letra de la palabra "Python" en una nueva línea.

