contador = 1 # Inicializa la variable contador con el valor 1. Esta variable se utilizará para controlar el número de iteraciones del bucle while.
while contador <= 5: # Inicia un bucle while que se ejecutará mientras la condición contador <= 5 sea verdadera. Esto significa que el bucle se repetirá mientras el valor de contador sea menor o igual a 5.
    print(contador) # Imprime el valor actual de contador en cada iteración del bucle. Esto mostrará los números del 1 al 5 en la consola.
    contador += 1 # Incrementa el valor de contador en 1 utilizando el operador de asignación +=. Esto es necesario para evitar un bucle infinito, ya que eventualmente hará que la condición contador <= 5 sea falsa y el bucle se detenga.

# En este ejemplo, el bucle while se utiliza para imprimir los números del 1 al 5. La variable contador se inicializa en 1 y se incrementa en cada iteración del bucle. El bucle se ejecuta mientras contador sea menor o igual a 5, lo que garantiza que se impriman los números

# Salida:
# 1
# 2
# 3
# 4
# 5
 