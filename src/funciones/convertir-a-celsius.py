# Función que convierte temperatura de Fahrenheit a Celsius
def convertir_a_celsius(fahrenheit):
    
    # Recibe un número (temperatura en Fahrenheit)
    """
    :param fahrenheit: Temperatura en grados Fahrenheit
    :type fahrenheit: float
    :return: Temperatura en grados Celsius
    :rtype: float
    """
    
    # Aplica la fórmula de conversión de Fahrenheit a Celsius
    return (fahrenheit - 32) * 5/9