nombres = ["Ana", "Carlos", "Elena"] # Lista de nombres 
for i in range(len(nombres)): # Bucle for que itera sobre los índices de la lista de nombres
    print(f"Posición {i}: {nombres[i]}") # Imprime la posición y el nombre correspondiente en cada iteración del bucle

# En este ejemplo, el bucle for utiliza la función range() junto con len() para generar una secuencia de índices que corresponden a las posiciones de los elementos en la lista "nombres". En cada iteración del bucle, se imprime la posición (índice) y el nombre correspondiente utilizando la sintaxis de formato de cadenas.

# Salida:
# Posición 0: Ana
# Posición 1: Carlos
# Posición 2: Elena