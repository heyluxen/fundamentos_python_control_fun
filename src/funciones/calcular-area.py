def calcular_area_rectangulo(base, altura):  # Función para calcular el área
    area = base * altura  # Multiplica base por altura
    return area  # Devuelve el resultado

# Uso de la función
resultado = calcular_area_rectangulo(5, 3)  # Calcula el área
print(f"El área del rectángulo es: {resultado}")  # Muestra el resultado

# Este ejemplo calcula el área de un rectángulo usando una función.

# Salida:
# El área del rectángulo es: 15