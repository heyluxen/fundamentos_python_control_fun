for i in range(5): # Bucle for que itera desde 0 hasta 4
    print(i) # Imprime el valor de i en cada iteración del bucle

# En este ejemplo, el bucle for utiliza la función range() para generar una secuencia de números desde 0 hasta 4. En cada iteración del bucle, el valor de i se imprime en la consola.
# Range es una función incorporada en Python que se utiliza para generar una secuencia de números. En este caso, range(5) genera los números 0, 1, 2, 3 y 4. El bucle for itera sobre esta secuencia y ejecuta el bloque de código dentro del bucle para cada número generado por range().

# Salida:
# 0
# 1
# 2
# 3
# 4