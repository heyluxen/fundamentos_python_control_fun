nota = 87

if nota >= 90:
    print("Calificación: Sobresaliente")
elif nota >= 80:
    print("Calificación: Notable")
elif nota >= 70:
    print("Calificación: Aprobado")
else:
    print("Calificación: Suspenso")

# Aqui lo que elif está haciendo es que si la nota es mayor o igual a 90, se muestra "Calificación: Sobresaliente", si la nota es mayor o igual a 80 pero menor a 90, se muestra "Calificación: Notable", si la nota es mayor o igual a 70 pero menor a 80, se muestra "Calificación: Aprobado", y si la nota es menor a 70, se muestra "Calificación: Suspenso".

# Salida:
# Calificación: Notable