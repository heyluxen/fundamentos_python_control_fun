dia = "sábado" # Se asigna el valor "sábado" a la variable dia.

if dia == "sábado" or dia == "domingo":
    print("Es fin de semana.") # Si el día es igual a "sábado" o "domingo", se muestra un mensaje indicando que es fin de semana.

# El operador or se utiliza para combinar dos condiciones en una declaración if. En este caso, se verifica si el día es igual a "sábado" o si el día es igual a "domingo". Si alguna de estas condiciones se cumple, se ejecuta el bloque de código dentro del if, mostrando el mensaje de que es fin de semana.

# Salida:
# Es fin de semana.