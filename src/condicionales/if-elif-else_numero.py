numero = 0

if numero > 0:
    print("El número es positivo.")
elif numero < 0:
    print("El número es negativo.")
else:
    print("El número es cero.")

# elif sirve para verificar multiples condiciones, si la primera condición es falsa, se verifica la siguiente, y así sucesivamente hasta encontrar una condición verdadera o llegar al else. En este caso, se verifica si el número es mayor que cero, menor que cero o igual a cero, y se muestra un mensaje correspondiente.

# Salida:
# El número es cero.