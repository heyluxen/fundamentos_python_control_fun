edad = 17 # Se asigna el valor 17 a la variable edad.
mensaje = "Eres mayor de edad." if edad >= 18 else "Eres menor de edad." # Se utiliza un condicional ternario para asignar un mensaje a la variable mensaje. Si la edad es mayor o igual a 18, se asigna "Eres mayor de edad.", de lo contrario, se asigna "Eres menor de edad.".
print(mensaje)  # Se imprime el valor de la variable mensaje, que en este caso será "Eres menor de edad." porque la edad es 17, que es menor a 18.

# El condicional ternario es una forma concisa de escribir una expresión condicional en una sola línea. En este caso, se evalúa la condición edad >= 18 y se asigna el mensaje correspondiente a la variable mensaje según el resultado de esa evaluación.

# Salida:
# Eres menor de edad.