edad = 45 # Se asigna el valor 45 a la variable edad.

if edad < 18: 
    print("Eres menor de edad.") # Si la edad es menor a 18, se muestra un mensaje indicando que eres menor de edad.
elif 18 <= edad < 65:
    print("Eres adulto.") # Si la edad es mayor o igual a 18 y menor a 65, se muestra un mensaje indicando que eres adulto. 
