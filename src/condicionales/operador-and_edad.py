edad = 25 # Se asigna el valor 25 a la variable edad.
ingresos = 30000 # Se asigna el valor 30000 a la variable ingresos.

if edad >= 18 and ingresos >= 20000:
    print("Eres elegible para el crédito.") # Si la edad es mayor o igual a 18 y los ingresos son mayores o iguales a 20000, se muestra un mensaje indicando que eres elegible para el crédito. El operador and se utiliza para combinar ambas condiciones, lo que significa que ambas deben ser verdaderas para que el bloque de código se ejecute.

# El operador and se utiliza para combinar dos o más condiciones en una declaración if. En este caso, se verifica que la edad sea mayor o igual a 18 y que los ingresos sean mayores o iguales a 20000. Si ambas condiciones se cumplen, se ejecuta el bloque de código dentro del if, mostrando el mensaje de elegibilidad para el crédito.

# Salida:
# Eres elegible para el crédito.