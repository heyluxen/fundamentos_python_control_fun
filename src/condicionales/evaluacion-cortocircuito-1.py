lista = [] # Se crea una lista vacía llamada lista.

if lista and lista[0] == 'Python': 
    print("El primer elemento es 'Python'.") # Se verifica si la lista no está vacía y si el primer elemento de la lista es igual a 'Python'. Si ambas condiciones son verdaderas, se imprime un mensaje indicando que el primer elemento es 'Python'.

# En este caso no se causa un error de índice porque la condición lista se evalúa primero. Si la lista está vacía, la condición lista será falsa y el programa no intentará acceder al primer elemento de la lista, evitando así un error de índice.
