edad = 17
if edad >= 18:
    print("Puedes votar en las elecciones.")
else:
    print("Aún no tienes edad para votar.")

# En este ejemplo, se verifica si la edad es mayor o igual a 18. Si la condición se cumple, se ejecuta el bloque de código dentro del if, que en este caso imprime un mensaje indicando que la persona puede votar en las elecciones. Si la condición no se cumple, se ejecuta el bloque de código dentro del else, que imprime un mensaje indicando que aún no tiene edad para votar.

# Salida:
# Aún no tienes edad para votar.