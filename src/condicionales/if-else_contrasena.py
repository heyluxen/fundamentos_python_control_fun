contrasena = input("Introduce la contraseña: ")
if contrasena == "secreta123":
    print("Acceso concedido.")
else:
    print("Contraseña incorrecta. Acceso denegado.")

# En este ejemplo se le pide al usuario introducir una contraseña. Si la contraseña introducida es igual a la del condicional, entonces se muestra un mensaje indicando que el acceso ha sido concedido. Si la contraseña no coincide, se muestra un mensaje indicando que la contraseña es incorrecta y el acceso ha sido denegado.

# Salida:
# Introduce la contraseña: secreta123
