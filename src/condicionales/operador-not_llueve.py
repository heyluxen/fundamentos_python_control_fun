llueve = False # Se asigna el valor False a la variable llueve, lo que indica que no está lloviendo.

if not llueve:
    print("Podemos salir al parque.") # Si no está lloviendo, se muestra un mensaje indicando que podemos salir al parque.

# El operador not se utiliza para negar una condición. En este caso, se verifica si no está lloviendo (not llueve). Si la condición es verdadera (es decir, si llueve es False), se ejecuta el bloque de código dentro del if, mostrando el mensaje de que podemos salir al parque.

# Salida:
# Podemos salir al parque.