numero = 15
if numero % 2 == 0: #  % 2 calcula el resto de dividir numero entre 2
    print("El número es par.")
else:
    print("El número es impar.")

# En este ejemplo, se verifica si el número es divisible por 2 (es decir, si el residuo de la división entre el número y 2 es igual a 0). Si la condición se cumple, se ejecuta el bloque de código dentro del if, que en este caso imprime un mensaje indicando que el número es par. Si la condición no se cumple, se ejecuta el bloque de código dentro del else, que imprime un mensaje indicando que el número es impar. 