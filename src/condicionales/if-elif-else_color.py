color = "azul" # Aquí se asigna el valor "azul" a la variable color.

if color == "rojo":
    print("El color es rojo.") # Si el color es igual a "rojo", se muestra un mensaje indicando que el color es rojo.
elif color == "verde":
    print("El color es verde.") # Si el color es igual a "verde", se muestra un mensaje indicando que el color es verde.
elif color == "azul":
    print("El color es azul.") # Si el color es igual a "azul", se muestra un mensaje indicando que el color es azul.

# elif se utiliza aqui para verificar que los colores sean rojo, verde o azul, si el color es igual a alguno de estos, se muestra un mensaje indicando el color correspondiente.

# Salida:
# El color es azul.